const supportedLanguages = new Set(["en", "zh"]);
const revealItems = document.querySelectorAll(".reveal");
const tiltItems = document.querySelectorAll("[data-tilt]");
const ticker = document.querySelector(".ticker");
const languageButtons = document.querySelectorAll("[data-lang]");
const descriptionMeta = document.querySelector('meta[name="description"]');

function normalizeLanguage(language) {
  if (!language) {
    return null;
  }
  const normalized = language.toLowerCase();
  if (normalized.startsWith("zh")) {
    return "zh";
  }
  if (normalized.startsWith("en")) {
    return "en";
  }
  return supportedLanguages.has(normalized) ? normalized : null;
}

function readStoredLanguage() {
  try {
    return normalizeLanguage(localStorage.getItem("adhibit-language"));
  } catch {
    return null;
  }
}

function storeLanguage(language) {
  try {
    localStorage.setItem("adhibit-language", language);
  } catch {
    // Language switching still works for the current page when storage is unavailable.
  }
}

function updateUrlLanguage(language) {
  const url = new URL(window.location.href);
  url.searchParams.set("lang", language);
  window.history.replaceState({}, "", url);
}

function preferredLanguage() {
  const queryLanguage = normalizeLanguage(new URLSearchParams(window.location.search).get("lang"));
  if (queryLanguage) {
    return queryLanguage;
  }

  const storedLanguage = readStoredLanguage();
  if (storedLanguage) {
    return storedLanguage;
  }

  return normalizeLanguage(navigator.language) || "en";
}

function translated(dictionary, key) {
  return dictionary[key] || translations.en[key] || "";
}

function applyLanguage(language, options = {}) {
  const nextLanguage = supportedLanguages.has(language) ? language : "en";
  const dictionary = translations[nextLanguage];

  document.documentElement.lang = nextLanguage === "zh" ? "zh-Hans" : "en";
  document.documentElement.dataset.language = nextLanguage;
  document.title = translated(dictionary, "meta.title");

  if (descriptionMeta) {
    descriptionMeta.setAttribute("content", translated(dictionary, "meta.description"));
  }

  for (const element of document.querySelectorAll("[data-i18n]")) {
    element.textContent = translated(dictionary, element.dataset.i18n);
  }

  for (const element of document.querySelectorAll("[data-i18n-attrs]")) {
    for (const pair of element.dataset.i18nAttrs.split(",")) {
      const [attribute, key] = pair.split(":").map((part) => part.trim());
      if (attribute && key) {
        element.setAttribute(attribute, translated(dictionary, key));
      }
    }
  }

  for (const button of languageButtons) {
    const isActive = button.dataset.lang === nextLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  }

  if (options.persist) {
    storeLanguage(nextLanguage);
  }

  if (options.updateUrl) {
    updateUrlLanguage(nextLanguage);
  }
}

applyLanguage(preferredLanguage());

if (ticker) {
  ticker.innerHTML += ticker.innerHTML;
}

for (const button of languageButtons) {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang, { persist: true, updateUrl: true });
  });
}

const revealObserver = new IntersectionObserver((entries) => {
  for (const entry of entries) {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      revealObserver.unobserve(entry.target);
    }
  }
}, { threshold: 0.16 });

for (const item of revealItems) {
  revealObserver.observe(item);
}

for (const item of tiltItems) {
  item.addEventListener("pointermove", (event) => {
    const rect = item.getBoundingClientRect();
    const x = ((event.clientX - rect.left) / rect.width - 0.5) * 8;
    const y = ((event.clientY - rect.top) / rect.height - 0.5) * -8;
    item.style.setProperty("--tilt-x", `${y.toFixed(2)}deg`);
    item.style.setProperty("--tilt-y", `${x.toFixed(2)}deg`);
  });

  item.addEventListener("pointerleave", () => {
    item.style.setProperty("--tilt-x", "0deg");
    item.style.setProperty("--tilt-y", "0deg");
  });
}
